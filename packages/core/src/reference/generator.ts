import type { ElementDefinition, PropDefinition } from '../elements/composer.js';
import type { ActionDefinition, ValidatorDefinition, ExpressionHandlerDefinition } from '../types.js';
import type { DesignTokens } from '../design/tokens.js';

export interface ReferenceDocConfig {
  /** Project name */
  name: string;
  /** Available primitives (type names) */
  primitives: string[];
  /** Custom element definitions (Layer 3) */
  elements?: ElementDefinition[];
  /** Design tokens */
  tokens?: DesignTokens;
  /** Registered actions */
  actions?: ActionDefinition[];
  /** Registered validators */
  validators?: ValidatorDefinition[];
  /** Extra expression handlers (beyond built-in) */
  extraExpressions?: ExpressionHandlerDefinition[];
}

/**
 * Generate a complete reference document (markdown) for AI consumption.
 * This is the primary product of Mythik — what makes the AI effective.
 */
export function generateReferenceDoc(config: ReferenceDocConfig): string {
  const sections: string[] = [];

  sections.push(generateHeader(config.name));
  sections.push(generateExpressionReference());
  sections.push(generatePrimitivesReference(config.primitives));

  if (config.elements && config.elements.length > 0) {
    sections.push(generateElementsReference(config.elements));
  }

  if (config.tokens) {
    sections.push(generateTokensReference(config.tokens));
  }

  sections.push(generateActionsReference(config.actions));
  sections.push(generateTransactionReference());
  sections.push(generateValidatorsReference(config.validators));
  sections.push(generateDeriveReference());
  sections.push(generateDataSourcesReference());
  sections.push(generateTemplatesReference());
  sections.push(generateRepeatEnhancedReference());
  sections.push(generateSpecStructure());
  sections.push(generateRules());
  sections.push(generatePatchReference());

  return sections.join('\n\n---\n\n');
}

