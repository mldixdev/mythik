function generatePrimitivesReference(primitives: string[]): string {
  const lines = primitives.map((p) => `- \`${p}\``);
  return `## Available Primitives

These are the built-in visual building blocks. Use them as the \`type\` field in elements.

${lines.join('\n')}

Each primitive accepts \`style\` (CSS object), \`visible\` (condition), and \`permission\` (role-based access).

**Theme auto-inheritance:** All interactive primitives (input, textarea, select, checkbox, toggle, slider, button, accordion, tabs) automatically inherit colors from the design system tokens. No \`$token\` references needed for basic theming — it works in both light and dark mode by default. Use explicit \`style\` props only when you need per-field overrides.

**Input format prop:** The \`input\` primitive supports a \`format\` prop for display masking:
- \`"phone"\` — displays as (555) 123-4567, stores raw digits
- \`"currency"\` — displays as $1,234.56, stores raw number

Example: \`{ "type": "input", "props": { "label": "Phone", "value": { "$bindState": "/phone" }, "format": "phone" } }\``;
}

function generateElementsReference(elements: ElementDefinition[]): string {
  const elementDocs = elements.map((el) => {
    const propsDoc = Object.entries(el.props).map(([name, def]) => {
      const parts = [`  - \`${name}\`: ${def.type}`];
      if (def.default !== undefined) parts.push(`(default: ${JSON.stringify(def.default)})`);
      if (def.values) parts.push(`(options: ${def.values.join(', ')})`);
      if (def.bindable) parts.push(`(bindable)`);
      return parts.join(' ');
    });

    return `### \`${el.type}\`

**Props:**
${propsDoc.join('\n')}

**Usage:**
\`\`\`json
{
  "type": "${el.type}",
  "props": { ${Object.entries(el.props).map(([n, d]) => `"${n}": ${JSON.stringify(d.default ?? `<${d.type}>`)}`).join(', ')} }
}
\`\`\``;
  });

  return `## Custom Elements (Layer 3)

These are reusable components composed from primitives. Use them like primitives.

${elementDocs.join('\n\n')}`;
}

function generateTokensReference(tokens: DesignTokens): string {
  const tokenLines: string[] = [];

  function walkTokens(obj: Record<string, unknown>, prefix: string) {
    for (const [key, value] of Object.entries(obj)) {
      if (key === 'modes') continue;
      const path = prefix ? `${prefix}.${key}` : key;
      if (value !== null && typeof value === 'object' && !Array.isArray(value)) {
        walkTokens(value as Record<string, unknown>, path);
      } else {
        tokenLines.push(`| \`${path}\` | \`${JSON.stringify(value)}\` |`);
      }
    }
  }

  walkTokens(tokens as unknown as Record<string, unknown>, '');

  const modesDoc = tokens.modes
    ? `\n\n**Available modes:** ${Object.keys(tokens.modes).join(', ')}`
    : '';

  return `## Design System Tokens

Reference tokens with \`{ "$token": "path" }\`. Available tokens:

| Path | Value |
|------|-------|
${tokenLines.join('\n')}${modesDoc}`;
}

